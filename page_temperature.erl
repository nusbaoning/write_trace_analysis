getTemperature(R, W) ->
	R+10*W.

isReadOnce(R, W) ->
	case R of
		1 when W=:=0 ->
			true;
		_ ->
			false
	end.

isWriteOnce(R, W) -> 
	case R of
		0 when W=:=1 ->
			true;
		_->
			false
	end.

isReadOnly(_R, W) ->
	case W of
		0 ->
			true;
		_ ->
			false
	end.

isWriteOnly(R, _W) -> 
	case R of
		0  ->
			true;
		_->
			false
	end.

