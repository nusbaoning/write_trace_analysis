getMaxTime() -> 534.
getMinTime() -> 713.
getPreviousTimeWindow({Start, _}) ->
	case Start-getMinTime() < getTimeWindow() of
		true ->			
			error;
		false ->
			{Start - getTimeWindow(), Start - 1}
	end.

getNextTimeWindow({_, End}) ->
	case getMaxTime()-End < getTimeWindow() of
		true ->
			error;
		false ->
			{End+1, End+getTimeWindow()}
	end.